/**
 * Funcion que permite retornar el valor de la opción elegida
 * @param { opcion elegida por el usuario } option
 */
export const returnUserType = (option) => {
	var userType = '';
	if (option == 0) {
		userType = 'user';
	}
	if (option == 1) {
		userType = 'veterinary';
	}
	if (option == 2) {
		userType = 'fundation';
	}
	return userType;
};

